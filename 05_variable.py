# Rules to assign variables 
# 1. The variables should contain,letters, numbers, and underscore
# 2. Do not start with numbers, e.g 2x = 4
# 3. No spaces in variables
# 4. Do not use keywords like mean, break, media 
# 5. Short and descriptive 
# 6. Case sensitivity (upper and lower case sensitivity)


